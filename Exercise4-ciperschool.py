# String slicing
lang = "python"
print(lang[0])
print(lang[0:3])
print(lang[0:4])
print(lang[:-2])
print(lang[:3])

# Step argument slicing
# syntax - [start argument:stop argument -1 : step]
print("Programming"[10::-1])
print("Programming"[0:5:1])
print("Programming"[0:10:2])
print("programming"[0:10:3])

